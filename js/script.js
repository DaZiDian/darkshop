    let currentLanguage = 'zh-CN';

function switchLanguage(lang) {
    currentLanguage = lang;
    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-lang');
        element.textContent = translations[currentLanguage][key] || '';
    });
}

// 添加事件监听器到i18n图标元素
document.getElementById('languageSwitch').addEventListener('click', function() {
    
    const lang = this.getAttribute('data-lang');

    // 使用scrollIntoView方法使页面滚动到底部栏
    document.getElementById('footerSection').scrollIntoView({ behavior: 'smooth' });
});
// 添加事件监听器到footer上的语言图标元素
document.querySelectorAll('.lang-icon img').forEach(icon => {
    icon.addEventListener('click', function(event) {

        // 获取图标上的数据属性，即语言代码
        const lang = this.getAttribute('data-lang');

        // 切换语言
        switchLanguage(lang);

        // 如果需要的话，可以在这里添加额外的逻辑，比如显示一个提示框告诉用户语言已更改
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });
});
// 初始化语言
switchLanguage(currentLanguage);